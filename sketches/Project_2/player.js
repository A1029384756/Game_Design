class Player extends GameObject {
  /**
   * @param {Image} sprite 
   * @param {Vector} pos 
  */
  constructor(sprite, pos) {
    super(pos)
    /* @type {Image} */
    this.sprite = sprite
    /* @type {Number} */
    this.direction = 0
    /* @type {Number} */
    this.speed = 1
    /* @type {Collider} */
    this.collider = new Collider(this.pos, this.sprite.width)
    /* @type {Number} */
    this.facing_angle = 0
  }

  update() {
    super.update()
    let dir = createVector()
    if (keyIsDown(UP_ARROW)) {
      dir.y--
    }
    if (keyIsDown(DOWN_ARROW)) {
      dir.y++
    }
    if (keyIsDown(LEFT_ARROW)) {
      dir.x--
    }
    if (keyIsDown(RIGHT_ARROW)) {
      dir.x++
    }

    if (dir.magSq() > 0) {
      dir.normalize()
      this.pos.x += dir.x * this.speed * deltaTime / 10
      this.pos.y += dir.y * this.speed * deltaTime / 10
      this.facing_angle = -dir.angleBetween(createVector(1, 0))
    }

    game_controller.add_message(
      new Message(
        MessageType.Camera,
        this
      )
    )
  }

  draw() {
    let pos = copy_vector(this.pos)
    translate(pos)
    rotate(this.facing_angle)
    image(this.sprite, -this.sprite.width / 2, -this.sprite.height / 2)
    rotate(-this.facing_angle)
    translate(pos.mult(createVector(-1, -1)))
  }

  /** @param {GameObject} rhs */
  handle_interaction(rhs) {
    if (rhs instanceof Enemy) {
      if (this.collider.collides(rhs.collider)) {
        game_controller.lose_game()
      }
    }
  }
}

